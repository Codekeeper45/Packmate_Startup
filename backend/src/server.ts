  console.log(`\nðŸš€ Packmate API running on http://localhost:${PORT}`);
  console.log(`   Environment : ${process.env.NODE_ENV ?? 'development'}`);
  console.log(`   Client CORS  : ${CLIENT_ORIGIN}\n`);
